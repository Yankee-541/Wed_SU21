function Yellow_Background(num){
    if(num == '1'){
        document.getElementById('input_1').style.backgroundColor = 'YELLOW';
    }else if(num =='2'){
        document.getElementById('input_2').style.backgroundColor = 'YELLOW';
    }
    else if(num == '3'){
        document.getElementById('input_3').style.backgroundColor = 'YELLOW';
    }
}

function White_Background(){
    document.getElementById('input_1').style.backgroundColor = 'WHITE';
    document.getElementById('input_2').style.backgroundColor = 'WHITE';
    document.getElementById('input_3').style.backgroundColor = 'WHITE';

}
